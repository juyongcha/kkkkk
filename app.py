# ─── [업그레이드] 재무제표 0 지우고 한글 단위로 보기 쉽게 바꾸는 마법의 함수 ───
def 금액_단위_변환(숫자, 한국주식여부):
    if pd.isna(숫자) or 숫자 == 0:
        return "-"
    
    부호 = "-" if 숫자 < 0 else ""
    숫자 = abs(float(숫자))
    
    if 한국주식여부:
        # 한국 주식은 보통 원본이 '원' 단위로 들어옵니다.
        if 숫자 >= 1000000000000: # 1조 이상
            조 = 숫자 // 1000000000000
            억 = (숫자 % 1000000000000) // 100000000
            return f"{부호}{조:,.0f}조 {억:.0f}억원"
        elif 숫자 >= 100000000: # 1억 이상
            억 = 숫자 / 100000000
            return f"{부호}{억:,.1f}억원"
        elif 숫자 >= 10000: # 1만 이상
            만 = 숫자 / 10000
            return f"{부호}{만:,.0f}만원"
        else:
            return f"{부호}{숫자:,.0f}원"
    else:
        # 미국 주식은 원본이 '달러' 단위로 들어옵니다.
        if 숫자 >= 100000000: # 1억 달러 이상
            억달러 = 숫자 / 100000000
            return f"{부호}{억달러:,.1f}억달러"
        elif 숫자 >= 10000: # 1만 달러 이상
            만달러 = 숫자 / 10000
            return f"{부호}{만달러:,.0f}만달러"
        else:
            return f"{부호}{숫자:,.0f}달러"

# ─── 재무제표 및 현금흐름 분석 화면 출력 ───
